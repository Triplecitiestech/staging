// ============================================
// HEALTH EMAIL
// ============================================

function buildHealthEmail(name: string, data: unknown, periodLabel: string): EmailResult {
  const d = data as {
    scores: Array<{
      displayName: string;
      overallScore: number;
      tier: string;
      trend: string | null;
    }>;
    distribution: {
      healthy: number;
      needsAttention: number;
      atRisk: number;
      critical: number;
    };
  };

  let body = `<div style="${STYLES.card}">`;
  body += '<h3 style="color: #e2e8f0; font-size: 16px; margin-bottom: 12px;">Health Distribution</h3>';
  body += statCard('Healthy', d.distribution.healthy);
  body += statCard('Needs Attention', d.distribution.needsAttention);
  body += statCard('At Risk', d.distribution.atRisk);
  body += statCard('Critical', d.distribution.critical);
  body += '</div>';

  body += `<div style="${STYLES.card}">`;
  body += '<table style="width: 100%; border-collapse: collapse;">';
  body += `<tr>
    <th style="${STYLES.tableHeader}">Company</th>
    <th style="${STYLES.tableHeader}; text-align: right;">Score</th>
    <th style="${STYLES.tableHeader}; text-align: center;">Tier</th>
    <th style="${STYLES.tableHeader}; text-align: center;">Trend</th>
  </tr>`;

  for (const s of d.scores) {
    const scoreColor = s.overallScore >= 80 ? '#10b981' : s.overallScore >= 60 ? '#06b6d4' : s.overallScore >= 40 ? '#f97316' : '#ef4444';
    body += `<tr>
      <td style="${STYLES.tableCell}">${s.displayName}</td>
      <td style="${STYLES.tableCell}; text-align: right; color: ${scoreColor}; font-weight: 700;">${Math.round(s.overallScore)}</td>
      <td style="${STYLES.tableCell}; text-align: center;">${s.tier}</td>
      <td style="${STYLES.tableCell}; text-align: center;">${s.trend || 'stable'}</td>
    </tr>`;
  }

  body += '</table></div>';

  return {
    subject: `${name} - Customer Health Report (${periodLabel})`,
    html: wrapEmail(name, periodLabel, body),
  };
}
